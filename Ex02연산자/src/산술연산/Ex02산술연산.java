package 산술연산;

public class Ex02산술연산 {

	public static void main(String[] args) {
		
		int num1 = 7;
		int num2 = 3;
		float num3 = 7;
		float num4 = 3;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		//형변환(정수(int) -> 실수(float, double))
		System.out.println(num3/num4);
		System.out.println((double)num1/num2);
		
		int num5 = 456;
		System.out.println(num5/100*100);
		System.out.println(num5 - (num5%100));
		
		int num6 = 111;
		System.out.println(num6/100*100);
		System.out.println(num6 - (num6%100));
		
		int num7 = 456;
		System.out.println(num7 - 5);
		int num8 = 777;
		System.out.println(num8 - 6);
		
		int num9 = 10;
		int num10 = 3;
		System.out.println(num9+num10);
		System.out.println(num9-num10);
		System.out.println(num9*num10);
		System.out.println(num9/num10);
		
		
		
		
		
		
		
		
		
		
	}

}
